/** Detect browser language and return matching LangCode */
export function detectLanguage(): LangCode {
  if (typeof window === "undefined") return "en";
  const stored = localStorage.getItem("limeihua-lang");
  if (stored && stored in translations) return stored as LangCode;
  const browserLang = navigator.language || (navigator as any).userLanguage || "en";
  const primary = browserLang.split("-")[0].toLowerCase();
  if (primary in translations) return primary as LangCode;
  // Try matching zh-TW, zh-CN etc.
  if (primary === "zh") return "zh";
  return "en";
}

/** Get translation for current language */
export function t(key: keyof TranslationKeys, lang?: LangCode): string {
  const currentLang = lang || detectLanguage();
  return translations[currentLang]?.[key] || translations.en[key] || key;
}

/** Set language preference */
export function setLanguage(lang: LangCode): void {
  localStorage.setItem("limeihua-lang", lang);
}

/** Get current language */
export function getCurrentLanguage(): LangCode {
  return detectLanguage();
}

